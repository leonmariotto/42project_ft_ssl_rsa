#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t len1;
	size_t len2;
	size_t i;

	if (!s1 || !s2)
		return (NULL);
	len1 = ft_strlen(s1);
	len2 = ft_strlen(s2);
	i = 0;
	while (i <= len2)
	{
		s1[len1 + i] = s2[i];
		++i;
	}
	return (s1);
}
