#include "libft.h"

int				ft_memcpy_inv(void *dst, void *src, int size)
{
	int i;
	int j;

	i = size - 1;
	j = 0;
	while (i >= 0)
	{
		((char*)dst)[j] = ((char*)src)[i];
		i--;
		j++;
	}
	return (1);
}
