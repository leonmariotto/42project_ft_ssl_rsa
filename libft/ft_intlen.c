#include "libft.h"

size_t	ft_intlen(const int n)
{
	if (n < 0)
		return ((n < -9 ? ft_intlen(-(n / 10)) + 1 : 1) + 1);
	else
		return (n > 9 ? ft_intlen(n / 10) + 1 : 1);
}
